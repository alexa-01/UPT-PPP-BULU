    <div class="col-lg-6 content" data-aos="fade-up" data-aos-delay="100">
      <p>
        Sejarah Dharma Wanita Persatuan berawal pada 5 Agustus 1974 saat organisasi para Isteri Pegawai Republik Indonesia pada masa Pemrintahan Orde Baru 
        itu dibentuk dengan nama Dharma Wanita. Organisasi ini didirikan oleh Ketua Dewan Pembina KORPRI saat itu, Amir Machmud, atas prakarsa Ibu Tien Soeharto 
        sebagai Ibu Negara, pada waktu itu Dharma Wanita beranggotakan para Istri Pegawai Republik Indonesia, Anggota ABRI yang dikaryakan dan Pegawai BUMN.
        <br><br> Pada era Reformasi tahun 1998, organisasi wanita ini melakukan perubahan mendasar, tidak ada lagi muatan politik dari Pemerintah. 
        Dharma Wanita menjadi organisasi sosial kemasyarakatan yang netral dari politik, Independen dan Demokrasi
        <br><br>Nama Dharma Wanita kemudian berubah menjadi Dharma Wanita Persatuan, penambahan kata “Persatuan” disesuaikan dengan nama Kabinet Persatuan Nasional
        dibawah kepemimpinan Presiden Abdurrahman Wahid. Perubahan organisasi ini tidak terbatas pada penambahan kata Persatuan namun juga berubah menjadi organisasi
        yang mandiri dan Demokrasi.
        <br><br>Pada Munas Luar Biasa (Munaslub) Dharma Wanita yang diselenggarakan pada tanggal 6 - 7 Desember 1999, seluruh rancangan Anggaran Dasar disahkan dan
        menetapkan Ketua Umum Dharma Wanita Persatuan terpilih, Ny. Dr. Nila F. Moeloek.
      </p>
      
    </div>

    <div class="col-lg-6" data-aos="fade-up" data-aos-delay="200">
      <p><strong>Pokok-pokok perubahan organisasi Dharma Wanita yang ditetapkan pada Munaslub antara lain :
              <br>1. Nama organisasi   berubah   menjadi   Dharma   Wanita Persatuan
              <br>2. Istilah Istri Pegawai Republik Indonesia diganti menjadi Istri Pegawai Negeri Sipil Republik Indonesia
              <br>3. Penegasan sebagai organisasi sosial kemasyarakatan yang bergerak dibidang Pendidikan, Ekonomi dan Sosial Budaya
              <br>4. Penegasan sebagai organisasi non politik
              <br>5. Penerapan demokrasi dalam organisasi dalam organisasi (Ketua Umum dan Ketua pada Unsur Pelaksana dipilih secara Demokrasi).
          </strong>
      </p>
      <p>Sebagai salah satu organisasi masyarakat (ormas) perempuan terbesar di Indonesia, sudah selayaknya Dharma Wanita Persatuan memiliki Standing Position dan 
        mengambil peran strategis dalam konstalasi pembangunan nasional, sebagaimana ormas lainnya, Dharma Wanita Persatuan memiliki peluang untuk berkiprah lebih luas 
        dengan mengoptimalisasikan peran sertanya sebagaimana yang diatur pada pasal 5 Undang-undang Nomor 17 Tahun 2013 Tentang Organisasi Kemasyrakatan, (“UU No.17 Th. 2013”) 
        yang menyebutkan bahwa pembentukan ormas bertujuan :
          <br>1. Meningkatkan partisipasi dan keberdayaan masyarakat
          <br>2. Memberikan pelayanan kepada masyarakat
          <br>3. Menjaga nilai agama dan kepercayaan terhadap Tuhan Yang Maha Esa
          <br>4. Melestarikan dan memelihara norma, nilai, moral, etika dan budaya yang hidup dalam masyarakat
          <br>5. Melestarikan sumber daya alam dan lingkungan hidup  
          <br>6. Mengembangkan kesetiakawanan  sosial,  gotong  royong dan bertoleransi dalam kehidupan bermasyarakat
      </p>
